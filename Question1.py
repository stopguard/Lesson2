"""
Выяснить тип результата выражений:
    15 * 3
    15 / 3
    15 // 2
    15 ** 2
"""
print(type(15 * 3))     # выводим тип произведения
print(type(15 / 3))     # выводим тип частного
print(type(15 // 3))    # выводим тип частного без остатка
print(type(15 ** 3))    # выводим тип возведения в степень
